    private void updateEm() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        for (int i = (model.getRowCount()); i < empleados.size(); i++) {
            Empleado tmp = empleados.get(i);
            Object[] rowData = new Object[]{tmp.getNomina(), tmp.getNomEmp(), tmp.geteApPat(), tmp.geteApMat(), tmp.getPuesto(), tmp.getSueldo(), tmp.geteCalle(), tmp.geteNum(), tmp.geteColonia(), tmp.geteCiudad(), tmp.geteEstado(), tmp.getFechaIng(), tmp.geteTelCasa(), tmp.geteCelular()};
            model.addRow(rowData);
        }
        jTable1.updateUI();
    }

    private void updateArrays() {
        empleados = handler.showEm();
    }

