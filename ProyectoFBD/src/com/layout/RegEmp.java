    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        SimpleDateFormat fecha = new SimpleDateFormat("yyyy/MM/dd", Locale.getDefault());
        String fing = fecha.format(jDateChooser1.getDate());
        String FchIng = fing.replaceAll("/", "-");
        try {
            if (!contrase単a.getText().equals("")) {
                if (contrase単a.getText().equals(conCtrs.getText())) {
                    handler.AltaEm(Nomina.getText(), Nombre.getText(), APP.getText(), APM.getText(), Puesto.getText(), Sueldo.getText(), Calle.getText(), Num.getText(), Col.getText(), Ciudad.getText(), Est.getText(), FchIng, Tel.getText(), Cel.getText());
                    handler.AltaUs(usuario.getText(), contrase単a.getText(), Integer.parseInt(Nomina.getText()));
                    Nomina.setText("");
                    Nombre.setText("");
                    APP.setText("");
                    APM.setText("");
                    Puesto.setText("");
                    Sueldo.setText("");
                    Calle.setText("");
                    Num.setText("");
                    Col.setText("");
                    Ciudad.setText("");
                    Est.setText("");
                    Tel.setText("");
                    Cel.setText("");
                    usuario.setText("");
                    contrase単a.setText("");
                    conCtrs.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Las Contrase単as no coinciden", "Error al registrar", JOptionPane.ERROR_MESSAGE);
                    contrase単a.setText("");
                    conCtrs.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(this, "El campo Contrase単a no puede estar vacio", "Error al registrar", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Revise los campos", "Error al registrar", JOptionPane.ERROR_MESSAGE);
        }
        updateArrays();
        updateEm();
    }//GEN-LAST:event_jButton3ActionPerformed

