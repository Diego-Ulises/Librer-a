    private void ActDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActDetalleActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            int id = (int) jTable1.getValueAt(row, 0);
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.getDataVector().removeAllElements();
            libped.forEach((tmp) -> {
                if (tmp.getIDPedido()== id) {
                    Object[] rowData = new Object[]{tmp.getIDPedido(), tmp.getISBN(), tmp.getCantidad(),tmp.getNombreEdit()};
                    model.addRow(rowData);
                }
            });
            jTable2.updateUI();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un pedido de la tabla");
        }
    }//GEN-LAST:event_ActDetalleActionPerformed

