    private void updateVen() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        for (int i = (model.getRowCount()); i < pedidos.size(); i++) {
            Pedidos tmp = pedidos.get(i);
            Object[] rowData = new Object[]{tmp.getIDPedido(), tmp.getNomina(), tmp.getFecha()};
            model.addRow(rowData);
        }
        jTable1.updateUI();
    }

