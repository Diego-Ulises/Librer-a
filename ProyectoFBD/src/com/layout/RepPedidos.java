    private void BuscarFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarFechaActionPerformed
        SimpleDateFormat fecha = new SimpleDateFormat("yyyy/MM/dd", Locale.getDefault());
        try {
            String fing = fecha.format(jDateChooser1.getDate());
            String FchIng = fing.replaceAll("/", "-");
            String Fd = FchIng.substring(8, 10);
            String Fm = FchIng.substring(5, 7);
            String Fy = FchIng.substring(0, 4);
            if(jRadioButton4.isSelected()){
                pedidos = handler.showPedD(Fd);
            }
            else if(jRadioButton3.isSelected()){
                pedidos = handler.showPedM(Fm);
            }
            else if(jRadioButton5.isSelected()){
                pedidos = handler.showPedY(Fy);
            }
            updateVen();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un fecha para la busqueda");
        }
    }//GEN-LAST:event_BuscarFechaActionPerformed

