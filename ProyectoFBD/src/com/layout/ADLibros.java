    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        String data;
        data = "" + jTextField1.getText();
        if (!data.equals("")) {
            if (jRadioButton1.isSelected()) {
                libros=handler.showLibT(data);
            }
            else if (jRadioButton2.isSelected()) {
                libros=handler.showLibA(data);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Escriba la información a buscar");
        }
        updateLib();
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void ButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            tmplib = libros.get(jTable1.getSelectedRow());
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
            jScrollPane2.setViewportView(jPanel3);
            for (int i = 0; i < editoriales.size(); i++) {
                if (jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString().equals(editoriales.get(i).getNombreEdit())) {
                    jComboBox3.setSelectedIndex(i);
                    break;
                }
            }
            String Fecha = jTable1.getValueAt(jTable1.getSelectedRow(), 8).toString().substring(0, 4);
            jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            jTextField3.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextField4.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            jTextField5.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            jTextField6.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString());
            jTextField8.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 6).toString());
            jTextField9.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 7).toString());
            jTextField10.setText(Fecha);
            jTextField11.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 9).toString());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un libro de la tabla");
        }
    }//GEN-LAST:event_ButtonModificarActionPerformed

