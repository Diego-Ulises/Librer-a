    private void updateArrays() {
        libros = handler.showLib();
        editoriales = handler.showEdit();
    }

    private void updateLib() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        for (int i = (model.getRowCount()); i < libros.size(); i++) {
            Libro tmp = libros.get(i);
            Object[] rowData = new Object[]{tmp.getISBN(), tmp.getTitulo(), tmp.getAutor(), tmp.getCost(), tmp.getPrec(), tmp.getNombreEdit(), tmp.getNumEd(), tmp.getLugarImp(), tmp.getAñoImp(), tmp.getCantidad()};
            model.addRow(rowData);
        }
        jTable1.updateUI();
    }

    private void updateEdit() {
        jComboBox3.removeAllItems();
        for (int i = 0; i < editoriales.size(); i++) {
            Editorial tmp = editoriales.get(i);
            jComboBox3.addItem("" + tmp.getNombreEdit());
        }
        jComboBox3.updateUI();
    }

